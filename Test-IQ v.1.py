"""
ВЕРСИЯ 1: Простейший консольный тест IQ
Без интерфейса
"""


def simple_iq_test():
    print("=" * 50)
    print("         ТЕСТ IQ (Консольная версия)")
    print("=" * 50)
    print("\nОтветьте на 5 простых вопросов.")
    print("Выберите номер правильного ответа (1, 2, 3 или 4).\n")

    # Вопросы
    questions = [
        ["2 + 2 = ?", "3", "4", "5", "6", 2],
        ["Столица России?", "Москва", "Санкт-Петербург", "Казань", "Новосибирск", 1],
        ["Сколько дней в неделе?", "5", "6", "7", "8", 3],
        ["Яблоко - это...", "Овощ", "Фрукт", "Ягода", "Зерно", 2],
        ["Солнце - это...", "Планета", "Звезда", "Спутник", "Комета", 2]
    ]

    score = 0

    # Задаём вопросы
    for i, q in enumerate(questions, 1):
        print(f"\nВопрос {i}: {q[0]}")
        for j in range(4):
            print(f"  {j + 1}) {q[j + 1]}")

        # Получаем ответ
        while True:
            try:
                answer = int(input("\nВаш ответ (1-4): "))
                if 1 <= answer <= 4:
                    break
                else:
                    print("Введите число от 1 до 4!")
            except:
                print("Введите число!")

        # Проверяем
        if answer == q[5]:
            print("Верно!")
            score += 1
        else:
            print(f"Неверно. Правильный ответ: {q[5]}) {q[q[5]]}")

    # Результаты
    iq = 80 + (score * 5)

    print("\n" + "=" * 50)
    print("РЕЗУЛЬТАТЫ ТЕСТА:")
    print(f"Правильных ответов: {score} из {len(questions)}")
    print(f"Ваш примерный IQ: {iq}")

    if score == 5:
        print("Отличный результат!")
    elif score >= 3:
        print("Хороший результат!")
    else:
        print("Есть куда расти!")

    print("=" * 50)


# Запуск
if __name__ == "__main__":
    simple_iq_test()
    input("\nНажмите Enter для выхода...")